#integers = 1, 2, 3, etc.
print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)

#exponents = **
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)

#order of operation
print(2 + 3 * 4)
print((2 + 3) * 4)

#floats = 1.0, 0.3, 10.6, etc.
print(0.1 + 0.1)
print(0.2 + .02)
print(2 * 0.1)
print(2 * 0.2)

#arbitrary number of decimal places
print(0.2 + 0.1)
print(3 * 0.1)

#integers / integers = floats 
print(4 / 2)

#integers and floats = floats
print(1 + 2.0)
print(2 * 3.0)
print(2 ** 3.0)

#Underscores in Numbers
universe_age = 14_000_000_000
print(universe_age)

#multiple assignment
x, y, z = 0, 0, 0
print(x)
print(y)
print(z)

